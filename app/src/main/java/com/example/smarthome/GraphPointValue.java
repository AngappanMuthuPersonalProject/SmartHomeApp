package com.example.smarthome;

public class GraphPointValue {

    int Xvalue;
    int Yvalue;

    public GraphPointValue(int xvalue, int yvalue) {
        Xvalue = xvalue;
        Yvalue = yvalue;
    }

    public int getXvalue() {
        return Xvalue;
    }

    public int getYvalue() {
        return Yvalue;
    }
}
